    private void iniciarMenus(boolean estado) {
        mnuCrearUsuarios.setVisible(estado);
        mnuListarUsuarios.setVisible(estado);
        mnuVehiculos.setVisible(estado);
        mnuListaMotos.setVisible(estado);
        mnuListaCoches.setVisible(estado);
        mnuVehiculos.setVisible(estado);
        mnuListaFurgonetas.setVisible(estado);
    }

    private void gestionarPermisosMenu() {

        switch (usuarioAutenticado.getTipoUsuario().getCodigo()) {
            //administrador
            case "1":
                iniciarMenus(true);
                break;
            ///usuario
            case "2":
                iniciarMenus(false);
                mnuListarUsuarios.setVisible(true);
                mnuVehiculos.setVisible(true);
                mnuListaMotos.setVisible(true);
                mnuListaCoches.setVisible(true);
                mnuListaFurgonetas.setVisible(true);
                break;
            ///cliente
            case "3":
                iniciarMenus(false);
                mnuListarUsuarios.setVisible(true);
                mnuVehiculos.setVisible(true);
                mnuListaMotos.setVisible(true);
                mnuListaFurgonetas.setVisible(true);
                mnuCrearUsuarios.setVisible(true);
                break;
        }
    }
        

